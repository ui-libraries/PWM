<p>“I saw farmers walking in water up to their waists pushing rafts on which were loaded their families and household goods,” a man from Hornick, Iowa (forty miles west of Ida Grove) told the Gazette. The History of Ida County quotes Persis Babcock who recalled “the scene of her father and other men wading through the flood waters with children and belongings heaped upon their shoulders.” In Ida Grove, the Babcock family housed forty guests and fed them all breakfast the morning following the flood.</p> 

<p>The flood also damaged many miles of railroad in Ida County. The railroad first came to Ida Grove only fourteen years prior. The first train passed through the town on October 26, 1877 and Ida Grove soon expanded from the industry brought by the new railway. By the early 1890’s, six railroad lines ran through the area surrounding Ida County: the Northwestern, Illinois Central, Milwaukee & St. Paul, Chicago, St. Paul and Minneapolis & Omaha railroads. The flood damaged all of these railways.</p> 
