<p>Two days after the nine-inch deluge, the Gazette reported, “At the present time it is impossible to estimate the damage to railroad property in that territory, but enough is known to show it will be very heavy.” In Ida County, the flood destroyed much of the Onawa railroad branch that ran fifty miles from Onawa to Ida Grove. The Gazette reported its railways were “entirely blockaded by the carrying away of bridges and washing away of road beds.”</p>

<p>Despite the damage caused to the railways, workers dispatched immediately to repair the lines. The Gazette writes, “The Northwestern management has a repair crew and half a dozen pile drivers at each end of the break on its lines, ready to begin repairs, but the waters are still so high that work can not be proceeded with. It will take several days to put the line in condition for the resumption of traffic.”</p>

<p>Railway workers quickly repaired the lines once the floodwaters receded in early July.</p>
	
