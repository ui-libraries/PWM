    <div class="singlecol">
<p>Two days later, the Cedar Rapids Gazette reported that rescue teams still had to wait for the floodwaters to recede before they could attempt to help stranded residents. The rising water submerged all of the railroads that led to Ida Grove. More rain fell on the morning of the 26th and the Gazette wrote, “All communication with the north is cut off.” </p>

<p>On the following morning the Gazette’s front-page story read: </p>
<blockquote ><i>A Trail of Ruin: The Destruction of Life and Property in Norhwestern [sic] Iowa Appalling—The Receding Waters Leave Hundreds of Head of Stock in the Broiling Sun and an Awful Stench Pervades the Air. Impossible to Obtain Full Particulars Yet—The Work of Removing the Debris Commenced in Earnest.</i></blockquote>

<p>On the 26th the Gazette reported, “Everything is in the wildest confusion.” Four people drowned from the rising water in Ida Grove. Many of the town’s residents sought temporary shelter in the G.A.R. and Masonic hall for several days.</p>

<p>By June 27, the Gazette called Ida County “A Sea of Water.”</p>

<p>Coverage of the flood even made it into the June 27th edition of the Buffalo Courier in Buffalo, New York: “Maple River here is the highest ever known. The town is inundated and all the bridges are washed away.”</p>

    </div>
    
