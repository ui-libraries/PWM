    <!-- left column text and image -->
    <div class="leftcol500 nomargin">
	    <p><span class="lead_in">Ida County residents usually remember the early 1890’s</span> as a time of severe drought, but one large rainstorm during the last week of June in 1891 halted the dry spell and caused the Maple River to overflow its banks and submerge much of Ida Grove. By the end of 1891 it was clear that the flood of that year was the worst the town of Ida Grove had ever experienced.</p>

<p>The storm hit Ida County on June 23rd in the evening and deposited nine inches of rain. The rivers quickly swelled and overflowed, destroying homes, railroad tracks and bridges, telephone lines, and farmers’ livestock and crops. At least twenty-five homes completely flooded in Ida Grove, and the flood forced many residents to seek shelter on higher ground.</p>

