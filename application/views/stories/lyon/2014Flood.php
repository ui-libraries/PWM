    	<p>While some 30 residents huddled in the elementary school gym, assisted by the Red Cross, at 11:15 a.m. on Tuesday, June 17, the Rock River in Rock Rapids crested at 26.98 feet, twice the level of flood stage.  The June 25 Lyon County Reporter estimated that 60-80 structures in Rock Rapids were affected and were temporarily or permanently uninhabitable.  Rock Rapids mayor Jason Chase told reporters that the biggest battles would be Island Park, the inundated recreation area, and other properties that were a total loss.  He wanted to get displaced people back in homes quickly.  “I don’t think anyone thought it would be this bad,” he said. </p> 

