	    <p><span class="lead_in">On June 16, 2014, Siouxland News Channel KTIV reported that the Rock River at Rock Rapids would rise another four feet</span> and would then be three feet higher than it had been in 1993.  This was the prediction of the National Weather Service. The Sheriff’s Department of Lyon County warned that not all of the river could be successfully sandbagged, and so they were ordering a mandatory evacuation in parts of Rock Rapids and in all of Alvord.  They recommended against travel anywhere in Lyon County.  The Sheriff added, “This storm is unlike anything they’ve ever experienced here in Rock Rapids.” </p> 

		<p>Apparently trouble began Saturday, June 14, when there was a breach on the Rock River at Blue Mound State Park in Luverne, Minnesota. That breach sent water flowing towards Rock Rapids.  Five inches of local rain that weekend compounded the problem.  By Sunday morning the river was at 13 feet.  Sandbagging began with many volunteers from Rock Rapids and other towns.  The plan was to prepare the city in twelve hours, but by Sunday evening, as things looked worse and worse, officials decided sandbagging needed to continue. </p> 

		<p>That night—2a.m. Monday morning—the river crested at 24.02 feet.  With a new gage having been added to the river in 2001 and with it a different method of measurement, there was some confusion about whether 1993 or 2010 held the flood record.  The crest in 2010 had been 22.25 feet.  Either way, the 2014 flood was greater.  So great that the Rock Rapids water plant had to be shut down and a boil order was issued for the city that Monday.  But the rains and the rising waters were not done yet.</p>
	    
