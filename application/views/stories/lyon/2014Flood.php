		<p>Lyon County residents had long expected less rain there than in other parts of Iowa.  The 1899 Iowa Geological Survey, volume 10, reported that the average rainfall in Lyon County was 8 inches less than in southern Iowa.  In fact, as recent as 2012 the county was experiencing a drought.  By September of that year parts of northwest Iowa had slipped into the most severe drought category:  exceptional drought.  And yet Harry Hillaker, State Climatologist, declared June 2014 the wettest month in Rock Rapids and in Sioux City in the history of state weather recordkeeping.  Whether 1993 or 2010 saw the second highest crest of the Rock River at Rock Rapids, there is no doubt that 2014 set a new record.</p>

