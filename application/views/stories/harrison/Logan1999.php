	    <p><span class="lead_in">Harrison County hadn’t seen a deadly tornado in over fifty years until May 16, 1999.</span> Many local high schoolers were celebrating their graduation that afternoon. The Logan-Magnolia High School concluded its graduation ceremony for the 41 graduating seniors around 3 pm. Just half an hour later, their superintendent announced a tornado warning over the school’s intercom.</p>

		<p>Most of Iowa was under a tornado watch that day and the National Weather Service issued tornado warnings for more than a dozen counties. Two tornadoes touched down in the area surrounding the town of Logan, spinning off of a super cell weather event that swept through Western Iowa in the late afternoon that Sunday. While Crawford, Mills and Pottawattamie counties were all impacted by these storms, Harrison County was hit the hardest. </p>

		<p>Two people died and fourteen were injured from the two tornadoes. Kathy Fugate (37 years old) and Juliet Pali (15 years old, of Bellevue) both lost their lives when trying to flee one of the tornadoes. Fugate and Pali, along with five other people, were celebrating the graduation of Kathy Fugate’s son when the storm hit. They decided Fugate’s trailer was unsafe and left to find a safer location. </p>

		<p>They decided to seek shelter in the cellar of Beverly Harter’s farmhouse just down the road from Fugate’s home. The group of seven piled into a car and headed towards Harter’s home. When they were less than an eighth of a mile away, the tornado destroyed Harter’s house and headed directly towards their car.</p> 

		<p>Seeing no other options, they jumped out of the car and sought shelter in the ditch. The tornado picked up and threw their car and a combine head on top of Beverly Harter, Kathy Fugate and Juliet Pali. The impact crushed and killed Fugate and Pali and Beverly Harter was paralyzed from the waist down. The five other people in the ditch were injured and three of them sustained serious injuries, including Fugate’s 15-year-old daughter, Lynette, and Lynette’s uncle, Kent Harter. Fugate’s mobile home remained in tact through the storm.</p>
		
		<p>A tornado that struck south of Logan caused the most damage, while another tornado moved west of Logan and damaged two homes. Both of these tornadoes were later classified as F-3 storms, with wind speeds between 158 and 205 mph.</p> 
		
