    	<p>The storms around Logan destroyed six homes, a bridge on a gravel road and damaged many other homes in the area. Highway 30 and Highway 127 were both closed because of the debris strewn across them from the tornadoes.</p>

		<p>Flooding followed the storms, as thunderstorms dumped more than three inches of rain across the county on Sunday, May 16th. Two days later, Iowa Governor Tom Vilsack declared Harrison County a disaster area because of the damage caused by the tornadoes. The storms caused an estimated $1.5 million in damage.</p>
		
		<p>Bobbie Sheranko, the mother of Juliet Pali who died in one of the tornadoes, turned 47 on the day she lost her daughter and was out of town in Texas when the tornado struck. Afterwards, while struggling with the loss of her daughter, Sheranko created a website to remember Juliet by and called it “Our Lovely Angel Juliet.” On the website, Sheranko recounts her search for others who had experienced the loss of a child. The Internet provided an outlet and a community of people to whom Sheranko expressed her feelings and thoughts.</p>
		
