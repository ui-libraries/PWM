	    <p>As a Union Pacific crane working on the railroad west of Lowden began to sink in the softened ground and lean over the tracks, one mile west of town, P&K Equipment was surrounded by water. Water covered the area from P&K into west Lowden on Highway 30, 5th Street and Harding Avenue, where it surrounded many homes and West Side Trucking. The only way to get into town that morning was from the north. High water covered other roads leading into town for several hours, and the county bridge on Y14 south, suffering washout damage was closed. The trains didn’t run again until the crane could be made to stand upright. </p>
