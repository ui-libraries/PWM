    <div class="leftcol500 nomargin">
	    <p><span class="lead_in">The Lowden area had just experienced its driest June</span> (from the 1st to the 20th) on record with just 0.22 inches of rain falling during the period. Who would have ever thought that we would have our worst flood in history, just four days later? This flash flood would surpass the famous one in 1944. Not only was the amount of rain greater in 2013, but also the rain fell in less than 6 hours compared to the flash flood of 1944, where rain fell over an 18-hour period.</p>
	    <p>The weather forecast issued on the 23rd called for the possibility of heavy rains for the evening and morning hours of the 24th. At my midnight observation, I noticed the persistent lightning off to the southwest. I had seen this type of situation before where the storms form to our southwest and repeatedly move over our area to dump very heavy rains. </p>
	    <p>The first wave of storms swept the area around 2 A.M. with high winds and torrential rains limiting visibility to less than a half block at times. Another similar storm followed an hour later. The storms continued to pound the area for the next five hours — dumping over 1 inch of rain per hour. There was continuous lightning, thunder and some hail. By 6 A.M., the Lowden area had received 4.10 inches and it was still raining. An hour later, the total was up to 5.30 inches. Just before 8 A.M., the recording rain gauge had recorded 6.62 inches had fallen in 5 ¾ hours. It soon went dead from floodwater and stopped recording. My other two rain gauges had floated away to be found later two blocks away. The Indee Creek (west part of town), East Creek (east part of town), and the Yankee Run Creek (south of town and U.S. 30) all overflowed because the Yankee Run filled up, causing the other two creeks to overflow into the western and eastern parts of town.</p>
