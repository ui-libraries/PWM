	    <p>Almost every creek in the county was flooded — washing out fences and flooding fields. Soil erosion was extremely severe, especially on more hilly ground. There was substantial loss of livestock and poultry along the larger streams. One farmer living in Lime City lost 150 head of sheep. On the Gust Licht farm one half-mile from Lowden, water was standing five feet deep in his buildings. He lost 30 hogs that were swept away in the Yankee Run Creek. He also lost all of his fences and hundreds of bushels of corn that were ruined by mud. Two other farmers south and east of Lowden lost livestock that were carried away by the high water. </p>
	    <p>Several main highways were blocked by high water and washed out bridges. Travel could only be accomplished with much detouring and backtracking. There were 96 damaged bridges — 56 completely taken out and 34 had approaches washed out to a degree that left them impassable. Cedar County residents couldn’t easily evacuate and couldn’t communicate with others either. The phone service was disrupted by downed telephone lines.</p>
