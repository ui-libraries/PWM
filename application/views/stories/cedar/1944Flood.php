        	<p>The storms hit the Lowden area late Sunday afternoon on the 25th of June and then again early Monday morning. All three creeks — Yankee Run, Indee and East Creek — went on the rampage and by midmorning on Monday, the surrounding area for several miles was completely under water and traffic was at a standstill. The homes in the west, south and east parts of Lowden were surrounded by water. Some homes in the west part of town were flooded over the main floor. The east wall of a basement collapsed in the west part of town. One home had water over the tabletop. Several families were evacuated, some by boat. The highway was flooded for several miles, blocking the westbound train. The Conrad and Conrad lumber yard and buildings were standing in two feet of water.</p>
