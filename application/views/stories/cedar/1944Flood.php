	    <p>Railroad service was badly disrupted across the county with passenger and mail trains rerouted far to the north or south. Service was suspended on the Chicago and Northwestern railway for 2 days because of a washed out bridge between Clarence and Stanwood. The Rock Island Line suspended service due to a bridge washed out at Moscow.</p>
	    <p>Scenes of devastation greeted the eye on all sides but no human lives were lost.</p>
