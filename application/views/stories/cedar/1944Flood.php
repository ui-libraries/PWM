	    <p><span class="lead_in">As the battle for Normandy raged in Europe,</span> people on the home front in Cedar County found themselves with a different kind of fight on their hands. Bodies of hogs and chickens dotted the water surface south of Lowden after a 24-hour, 10-inch rain caused extensive flash flooding on June 26, 1944.  It was probably the worst flood in Cedar County’s history. Like the Lowden area, the area east of Tipton also received 10 inches. The storm was accompanied by some hail and high winds that damaged trees and farm buildings. The Clarence area received 7.50 inches of rain, most of which fell between 7 and 9:30 A.M. on the 26th of June. Only the extreme southern part of the county escaped the deluge. </p>
