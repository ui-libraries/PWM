	    <p><span class="lead_in">For those living in Cedar County in 1979 </span>who remembered the winter of 1936, the winter of 1978-79 might have seemed familiar and different. Roads were repeatedly impassable — but delayed trains were no longer an issue; school was closed for weeks — but not for lack of coal to heat them. In fact the 1979 winter would surpass the winter of 1936 with colder temperatures and more snow. </p>
	    <p>The winter began on November 26-27 in 1978, with 4.4 inches of snow. The month of December began with more white stuff falling on the 2nd, accompanied by lightning and thunder. 10.7 inches accumulated. Another 2.2 inches fell on the 5th and one inch fell again on the 7th. Every day from the 28th to 31st snow fell, totaling 15.5 inches. December alone produced 30.5 inches of snow, a record. The snows of November and December remained until March 25, 1979 — a total of 120 days. Some of the snowdrifts remained well into April. </p>
	    <p>The cold air settled in from December 8th to 10th with lows from -12 to -16 degrees. Milder weather dominated from the 11th to the 20th with daily highs above freezing and little precipitation before the weather turned colder and wetter again. Morning lows were from 0 to -3 degrees from the 24th to the 27th. The high of 34 degrees on the 29th would be the last above freezing reading until February 20th. The month of December averaged 4.7 degrees below normal with seven days of 0 degree readings. </p>
