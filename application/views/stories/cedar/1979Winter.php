	    <p>On the 24th, the thermometer was down to -10 degrees with 42 mph winds. January ended as it began with impassable roads and impossible travel.</p>
	    <p>January 1979, with an average of 1.7 degrees, is the coldest on record in Cedar County. We had 24 days with 0 degree readings and 6 days with the high temperatures failing to get above 0 degrees. The 37.9 inches of snow was a record also. </p>
	    <p>On February 1st, only 5% of the county roads were opened to two lanes of traffic. Some farm families were stranded for several days. The National Guard was called in with their heavy equipment to open the county roads. They used their helicopter to airlift hay to stranded livestock. The state brought in two snow blowers to open the blacktopped roads but some of the snow on the gravel roads was so hard that the blowers did not work. For several weeks there was no school.</p>
	    <p>As January turned into February, Cedar County residents must have hoped for a change in the weather. Instead, the month of February got off to a cold start with readings below zero every morning from the 1st to the 10th. Lows ranged from -1 to -23 degrees. Four of these mornings saw readings of -20 or colder. There were 2.3 inches of snow on the 7th to 8th blown around by 32 mph winds. The weather was colder again from the 15th to 19th with lows of 0 to -18 degrees. Finally, the thermometer rose above freezing on the 20th  of February, the first time in 52 days. February’s average temperature was 8.7 degrees, the second coldest in Cedar County. February 1936 was colder. 16 days showed 0 degree readings; on three of those days the highs failed to get above zero. There were 8.7 inches of snow. </p>


