	    <p>After A New Year’s Eve storm, the weather turned bitterly cold — reading below zero from the 1st to the 11th. The mercury dipped below zero on the evening of the 1st and did not rise above the magic point until the afternoon of the 5th. This was a total of 93.5 hours. The lows during that period ranged form -10 to -25 degrees with seven of those being -20 or colder. Wind chills were from -20 to -70 degrees. </p>
	    <p>Snow began falling on the 11th and continued through the 13th, totaling 19.0 inches, 14.6 inches on the 13th alone. On that day, a full-blown blizzard raged with NW winds gusting to 43 mph and wind chills down to -40 degrees. The winds continued throughout the next day, bringing the thermometer down to -22 and, with wind chills, as low as an arctic -75 degrees. With snowdrifts 10 to 15 feet high, all roads were impassable. Many cars were stranded on Interstate 80 in the southern part of Cedar County. Tipton housed 140 stranded travelers. </p>
	    <p>Although Tipton residents were hospitable, the rest of the January, 1979 was not. Another 2.1 inches of snow fell on the 15th ; 4.1 inches, on the 18th and 19th ; 6.3 inches, on the 23rd; 4.5 inches, on the 26th and 27th. Intense cold and strong winds persisted. The morning lows from the 14th-18th were between -7 and -23 degrees. </p>
