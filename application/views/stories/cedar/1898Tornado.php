		<p>The storm moved a mile or so to the northeast, descending once again, this time to the J.C. Carl farm. It caused a large amount of damage, destroying the summer kitchen, twisting a new windmill into a fantastic shape, and tearing the wheel off of the hay rake. At the Elwyn Sawyer farm, halfway between Stanwood and Clarence, the barn and outbuildings were completely destroyed. The Stanwood railroad section workers had taken refuge in the barn but decided to seek refuge elsewhere. This was a fortunate move as the barn was blown into pieces. At Charles Hoegebuck’s farm, the tornado tossed the windmill over the house.</p>
		<p>After crossing the Chicago and Northwestern railroad tracks, the storm hit the farms of Al Miller, Gus Bixler, Henry Ayerhoff and Milt Scott. It also hit the Clarence Cemetery, damaging trees and breaking a number of monuments. 
</p>
