    <div class="leftcol500 nomargin">
	    <p>
		    <span class="lead_in">On Wednesday, May 18, 1898, a destructive tornado</span> swept across northern Cedar County. The day had been quite warm and sometime between two and three o’clock in the afternoon, rain and hail fell in considerable quantities. 
	    </p>
		<p>First noticed near the western border of the county, it struck the ground just south of Mechanicsville, raising the dust as if an explosion of dynamite had taken place. It shook the outbuildings at the William Jackson place at Red Oak. At the J.K. Davidson place, south of Stanwood, it wrecked the shed and cribs, and did considerable damage to the barn. A shed where two men were working was picked up and torn to pieces, leaving the men lying flat on the ground, uninjured. </p>
		<p>The Eli Auten farm was damaged next, where two men and a boy were laying tile when the storm approached. They headed for cover but the boy was caught and was given a pretty severe thumping.</p>
	    <img class="alignleft" width="100%"  alt="" src="<?php echo image_url('cedar/1898tornado/DekeHouse-Clarence.jpg');?>">
		<p class="caption">Deke House and Barn near Clarence.</p>
		<p>The farm of Charles Deke, two miles northeast of Clarence, was hit the hardest. A hired man, acting as a first responder, got the wife and two sleeping children into the basement just as the storm swept the house off the foundation. Every building was destroyed except the two corncribs. A large number of livestock were killed and most of the farm machinery destroyed. A ten-foot long 6-by-8 sill was driven seven feet into the ground. Pieces of wood were carried 80 rods away.
</p>
		<p>The farms of John Greig, Frank Ihns, Norm Eldridge, Henry Stange, Claus Stange and D.B. Myers all received damage. At the H.A. Emerson farm, his wife and daughter provided the only warning available. They called him to the house just in time as every other building on the place was swept to destruction.
</p>
		<p>At the John Emerson place, across the Wapsi River, the storm destroyed every building. They had not been home during the storm, but when they returned, they found that they had lost everything — the furniture, food and belongs were all lost. All they had was the clothing on their backs and each other.</p>
		<p>Just as the storm had flattened farms and left their inhabitants injured and exposed, the storm cut a regular swath through the timber, laying the large trees flat and uprooting the ones it could not twist off. </p>
		
