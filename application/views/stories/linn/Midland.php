    <h2>Flood of 2008</h2>

    <h3>Iowa Midland Supply, Inc.</h3>

    <p>Iowa Midland Supply, distributor of cutting tools, adhesives, safety equipment and more, has been in business since 1953. In June of 2008, the Stephan’s family business had 16 employees. Iowa Midland Supply was located at 3rd Street and 7th Ave S.E. in a building brothers Craig and Brian Stephan had bought from their uncle. Occupied for 41 years, the building had never been flooded until 2008.</p>

    <div class="leftcol500 nomargin">
	    <p>On the ninth of June, 2008, <i>The Cedar Rapids Gazette</i> warned readers that a river crest of 21.5 feet was forecast for their city, topping the 1929 record of 20.01 feet and 2.3 feet higher than the crest during the flood of 1993. “Record Flooding on the Way for Cedar Rapids” the headline announced. Five days later, June 14, 2008, <i>The Cedar Rapids Gazette</i> headline read, “Cedar Rapids Struggles to Grasp Losses.”
	    </p>
