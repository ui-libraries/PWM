		<p>The river had crested at 31.1 feet, not 21.5 feet. The downtown was under water. 25 thousand had been evacuated, hundreds by boat. 12 thousand homes in Linn County, alone, were without power. The city’s wells had been overtaken and were pumping at only 25%. Highways 380 south, 30 east, and 965 were all flooded. The early dollar estimate for damages was in the 100s of millions. (An August 27, 2009 article in the <i>New York Times</i> put the damage in Cedar Rapids at 6 billion dollars [Susan Saulny, “After Iowa Flood, Feeling Just a Bit Ignored”].)</p>
