    <h2>Floods of 1929, 2008</h2>

    <h3>Veterans Memorial Building</h3>

    <!-- full width text -->
    <img class="alignleft spaceright" width="200px"  alt="1929 Veterans Memorial Dedication Program, 1928" src="<?php echo image_url('linn/Vets/MemorialDedication.jpg');?>">
    <p class= "spaceright">Conceived in 1925 and built in 1927, Veterans Memorial Building on Mays Island in the Cedar River was dedicated on <br />September 12, 1928.</p>
    <img class="alignright spaceleft" width="450px"  alt="Grant Wood Mural Detail, Our Lady of Mourning" src="<?php echo image_url('linn/Vets/LadyOfMourning.jpg');?>">
   	<p class= "spaceright">The building housed not only City Hall but an auditorium large enough for a circus and a 25-foot tall glass mural designed by artist Grant Wood.  The mural depicts a sixteen-foot Lady of Mourning, bearing a palm branch of peace and a laurel wreath of victory, above six life-sized figures in uniforms of the American Revolutionary War, the War of 1812, the Mexican War, the American Civil War, the Spanish American War and the Great War.  As part of the May’s Island Historic District, Veterans Memorial Building and Memorial Window joined the National Register of Historic Places in 1978.</p>
